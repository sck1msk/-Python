def identify_shape(sides):
    shapes = {
        3: "Треугольник",
        4: "Четырехугольник",
        5: "Пятиугольник",
        6: "Шестиугольник",
        7: "Семиугольник",
        8: "Восьмиугольник",
        9: "Девятиугольник",
        10: "Десятиугольник"
    }
    return shapes.get(sides, "Ошибка: Введите число от 3 до 10.")

# Запрос у пользователя количества сторон
sides = int(input("Введите количество сторон фигуры: "))

# Определение и вывод вида фигуры
result = identify_shape(sides)
print(result)
