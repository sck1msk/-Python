def mpg_to_l_per_100km(mpg):
    return 235.215 / mpg

# Запрос у пользователя значения MPG
mpg = float(input("Введите расход топлива в милях на галлон (MPG): "))

# Проверка на нулевое значение, чтобы избежать деления на ноль
if mpg <= 0:
    print("Значение MPG должно быть больше нуля.")
else:
    l_per_100km = mpg_to_l_per_100km(mpg)
    print(f"Расход топлива в литрах на 100 км: {l_per_100km:.2f} L/100km")
