def main():
    # Запрашиваем у пользователя количество бутылок каждого размера
    small_bottles = int(input("Введите количество бутылок объемом 1 литр и меньше: "))
    large_bottles = int(input("Введите количество бутылок объемом больше 1 литра: "))

    # Расчет суммы возмещения
    refund = small_bottles * 0.10 + large_bottles * 0.25

    # Вывод суммы с форматированием
    print(f"Сумма, которую можно выручить: ${refund:.2f}")

if __name__ == "__main__":
    main()
