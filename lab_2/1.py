import math

def calculate_hypotenuse(a, b):
    """
    Функция принимает длины двух катетов a и b,
    и возвращает длину гипотенузы.
    """
    return math.sqrt(a**2 + b**2)

def main():
    """
    Главная функция программы, которая запрашивает у пользователя длины катетов,
    вызывает функцию расчета гипотенузы и выводит результат.
    """
    try:
        # Запрос у пользователя длины катетов
        a = float(input("Введите длину первого катета: "))
        b = float(input("Введите длину второго катета: "))

        # Проверка на положительность значений
        if a <= 0 or b <= 0:
            print("Длины катетов должны быть положительными числами.")
            return

        # Вызов функции для расчета гипотенузы
        hypotenuse = calculate_hypotenuse(a, b)

        # Вывод результата
        print(f"Длина гипотенузы: {hypotenuse:.2f}")
    except ValueError:
        print("Пожалуйста, введите корректные числовые значения.")

if __name__ == "__main__":
    main()
