BASE_FARE = 4.00  # Базовый тариф в долларах
PER_METER_RATE = 0.25 / 140  # Стоимость за метр поездки в долларах

def calculate_taxi_fare(distance_km):
    """
    Функция принимает расстояние поездки в километрах и возвращает сумму оплаты такси.
    """
    distance_meters = distance_km * 1000  # Перевод расстояния в метры
    variable_fare = distance_meters * PER_METER_RATE  # Рассчёт стоимости по расстоянию
    total_fare = BASE_FARE + variable_fare  # Итоговая стоимость
    return total_fare

def main():
    """
    Главная функция программы, которая запрашивает у пользователя расстояние поездки,
    вызывает функцию расчета стоимости и выводит результат.
    """
    try:
        # Запрос расстояния у пользователя
        distance_km = float(input("Введите расстояние поездки в километрах: "))

        # Проверка на положительность расстояния
        if distance_km < 0:
            print("Расстояние не может быть отрицательным.")
            return

        # Вызов функции для расчета стоимости поездки
        fare = calculate_taxi_fare(distance_km)

        # Вывод результата
        print(f"Стоимость поездки: ${fare:.2f}")
    except ValueError:
        print("Пожалуйста, введите корректное числовое значение расстояния.")

if __name__ == "__main__":
    main()
